        function updateQuantity(key, element) {
            $.post('{{ route('cart.updateQuantity') }}', {
                _token: '{{ csrf_token() }}',
                key: key,
                quantity: element.value
            }, function (data) {
                //updateNavCart();
                $('#cart-summary').html(data);
            });
        }

